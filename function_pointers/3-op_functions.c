#include "3-calc.h"

/**
 * op_add - check the desc
 * @a: integer
 * @b: integer
 * Desc: sum
 * Return: a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - check the desc
 * @a: integer
 * @b: integer
 * Desc: difference
 * Return: a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - check the desc
 * @a: integer
 * @b: integer
 * Desc: product
 * Return: a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - check the desc
 * @a: integer
 * @b: integer
 * Desc: div
 * Return: a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - check the desc
 * @a: integer
 * @b: integer
 * Desc: modulo
 * Return: a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
